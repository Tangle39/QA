//使用 Date 对象的 toString() 方法来打印当前日期和时间
import java.util.Date;

public class DateDemo {
    public static void main(String args[]) {
        // 初始化 Date 对象
        Date date = new Date();

        // 使用 toString() 函数显示日期时间
        System.out.println(date.toString());
    }
}
